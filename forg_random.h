struct RandomSequence
{
    u32 currentTableIndex;
};

inline RandomSequence Seed( i32 seed )
{
    RandomSequence result = {};
    result.currentTableIndex = seed % ArrayCount( randomNumberTable );
    return result;
}

inline u32 GetNextUInt32( RandomSequence* sequence )
{
    Assert( sequence->currentTableIndex < 4096 );
    u32 result = randomNumberTable[sequence->currentTableIndex++];
    if( ( sequence->currentTableIndex ) >= 4096 )
    {
        sequence->currentTableIndex = 0;
    }
    return result;
}

inline u64 RandomNonNullU64( RandomSequence* sequence )
{
    u64 first = ( u64 ) GetNextUInt32( sequence );
    u64 second = ( u64 ) GetNextUInt32( sequence );
    
    u64 result = ( first << 32 ) | ( second );
    result = Max( 1, result );
    return result;
}

inline u32 GetCurrentUInt32( RandomSequence* sequence )
{
    Assert( sequence->currentTableIndex < 4096 );
    u32 result = randomNumberTable[sequence->currentTableIndex];
    return result;
}

inline i32 RandomChoice(RandomSequence* sequence, u32 maximum)
{
    i32 result = GetNextUInt32( sequence ) % maximum;
    return result;
}

inline r32 RandomUni( RandomSequence* sequence )
{
    r32 result = ( r32 ) GetNextUInt32( sequence ) * ( r32 ) ONE_OVER_MAX_TABLE_VALUE;
    Assert( result >= 0 && result <= 1.0f );
    return result;
}

inline r32 RandomBil( RandomSequence* sequence )
{
    r32 result = RandomUni( sequence ) * 2.0f - 1.0f;
    return result;
}

inline Vec2 RandomBilV2(RandomSequence* sequence)
{
    Vec2 result = V2(RandomBil(sequence), RandomBil(sequence));
    return result;
}

inline Vec3 RandomBilV3(RandomSequence* sequence)
{
    Vec3 result = V3(RandomBil(sequence), RandomBil(sequence), RandomBil(sequence));
    return result;
}

inline Vec3 RandomUniV3(RandomSequence* sequence)
{
    Vec3 result = V3(RandomUni(sequence), RandomUni(sequence), RandomUni(sequence));
    return result;
}

inline r32 RandomRangeFloat( RandomSequence* sequence, r32 min, r32 max )
{
    r32 value = RandomUni( sequence );
    r32 range = max - min;
    r32 result = value * range + min;
    return result;
}

inline i32 RandomRangeInt( RandomSequence* sequence, i32 minValue, i32 maxValue )
{
    maxValue += 1;
    Assert( maxValue >= minValue );
    i32 result = 0;
    i32 nextValue = ( i32 ) GetNextUInt32( sequence );
    i32 range = maxValue - minValue + 1;
    result = nextValue % range + minValue;
    return result;
}

inline Vec3 RandomizeBil(Vec3 reference, RandomSequence* seq)
{
    Vec3 result;
    result.r = RandomBil(seq) * reference.r;
    result.g = RandomBil(seq) * reference.g;
    result.b = RandomBil(seq) * reference.b;
    
    result.r = Clamp01(result.r);
    result.g = Clamp01(result.g);
    result.b = Clamp01(result.b);
    
    return result;
}
inline i32 ThrowDices( RandomSequence * seq, DiceThrow* distr )
{
    i32 result = distr->offset;
    for( u32 throws = 0; throws < distr->throwCount; throws++ )
    {
        i32 dice = RandomChoice( seq, distr->faces );
        result += dice;
    }
    
    return result;
}

inline r32 ThrowDices( RandomSequence * seq, RangeDistribution * distr )
{
    r32 result = distr->min;
    
    Assert( distr->min <= distr->max );
    
    if( distr->min != distr->max )
    {
        u32 value = ThrowDices( seq, &distr->distr );
        r32 lerpValue = ( r32 ) value * distr->normalizationCoeff;
        result = Lerp( distr->min, lerpValue, distr->max );
        
    }
    
    return result;
}

inline u8 RandomBucket( RandomSequence* seq, BucketDistribution* distr )
{
    u32 bucketIndex = RandomChoice( seq, 100 );
    u8 result = distr->buckets[bucketIndex];
    return result;
}
